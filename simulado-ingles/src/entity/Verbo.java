package entity;

public class Verbo {
    private String verbo;
    private String traducao;

    public String getVerbo() {
        return verbo;
    }
    public void setVerbo(String verbo) {
        this.verbo = verbo;
    }
    public String getTraducao() {
        return traducao;
    }
    public void setTraducao(String traducao) {
        this.traducao = traducao;
    }
 
}
